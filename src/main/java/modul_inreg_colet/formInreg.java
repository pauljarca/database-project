    private boolean Verificare(){
        try{
        if(!Verify.verifyNume(destNume.getText())){
            throw new Exception("Nume destinatar: format gresit!");  
        }
        if(!Verify.verifyNume(expNume.getText())){
            throw new Exception("Nume expeditor: format gresit!");
        }
        if(!Verify.verifyMail(destMail.getText())){
            throw new Exception("Mail destinatar: format gresit!");
        }
        if(!Verify.verifyMail(expMail.getText())){
            throw new Exception("Mail expeditor: format gresit!");
        }
        if(!Verify.verifyTelefon(destTelefon.getText())){
            throw new Exception("Telefon destinatar: format gresit!");
        }
        if(!Verify.verifyTelefon(expTelefon.getText())){
           throw new Exception("Telefon expeditor: format gresit!");
        }
        
        if(destOrasCombo.getSelectedIndex()==0){
            throw new Exception("Oras destinatar: format gresit!");
        }
        if(expOrasCombo.getSelectedIndex()==0){
             throw new Exception("Oras expeditor: format gresit!");
        }
        
        if(!Verify.verifyCod(colCod.getText())){
             throw new Exception("Cod colet negenerat!");
        }
        if(!Verify.verifyNrNegativ(Float.parseFloat(colPret.getText()))){
            throw new Exception("Pret necalculat!");
        }
        if(!Verify.verifyNrNegativ(Float.parseFloat(colGreutate.getText()))){
            throw new Exception("Greutatea nu a fost introdusa!");
        }
        return true;
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, ex);
            return false;
        }
    }
    
